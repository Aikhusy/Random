    public void cari(String keyword){
        String[] kolom={"id","Nama","Alamat","Telepon","jk"};
        ArrayList<Customer> list = new Customer().search(keyword);
        Object rowData[]=new Object[5];
        
        tableCustomer.setModel(new DefaultTableModel(new Object[][] {},kolom));
        
        for(Customer kat:list){
            rowData[0]=kat.getIdCustomer();
            rowData[1]=kat.getNamaCustomer();
            rowData[2]=kat.getAlamat();
            rowData[3]=kat.getTelepon();
            rowData[4]=kat.getJk();
            
            ((DefaultTableModel)tableCustomer.getModel()).addRow(rowData);
        }
        
    }
    public void tampilData(){
        String[] kolom={"id","Nama","Alamat","Telepon","jk"};
        ArrayList<Customer> list = new Customer().getAll();
        Object rowData[]=new Object[5];
        
        
        tableCustomer.setModel(new DefaultTableModel(new Object[][] {},kolom));
        for(Customer kat:list){
            rowData[0]=kat.getIdCustomer();
            rowData[1]=kat.getNamaCustomer();
            rowData[2]=kat.getAlamat();
            rowData[3]=kat.getTelepon();
            rowData[4]=kat.getJk();
            
            ((DefaultTableModel)tableCustomer.getModel()).addRow(rowData);
        }
    }
