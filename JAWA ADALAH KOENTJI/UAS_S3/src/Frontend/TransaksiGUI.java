    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed
        // TODO add your handling code here:
        Transaksi t = new Transaksi();
        
        Customer c= new Customer();
        c=c.getByName(jComboBox2.getSelectedItem().toString());
        
        Menu m = new Menu();
        m=m.getByName(comboMenu.getSelectedItem().toString());
        
        t.setCustomer(c);
        t.setMenu(m);        
        
        t.setQuantity(Integer.parseInt(textQuantity.getText()));
        t.setTanggal(textTanggal.getText());
        
        
        t.save();
        tampilData();
    }//GEN-LAST:event_insertActionPerformed

