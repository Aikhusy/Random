    private void tableCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCustomerMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model= (DefaultTableModel)tableCustomer.getModel();
        int row= tableCustomer.getSelectedRow();
        textId.setText(model.getValueAt(row,0).toString());
        textNama.setText(model.getValueAt(row,1).toString());
        textAlamat.setText(model.getValueAt(row, 2).toString());
        textTelepon.setText(model.getValueAt(row, 3).toString());
        comboJk.getModel().setSelectedItem(model.getValueAt(row, 4).toString());
    }//GEN-LAST:event_tableCustomerMouseClicked

