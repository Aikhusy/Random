    public void tampilData(){
        String[] kolom={"id","Nama","Menu","quantity","tanggal","total"};
        ArrayList<Transaksi> list = new Transaksi().getAll();
        Object rowData[]=new Object[6];
        
        
        tableTransaksi.setModel(new DefaultTableModel(new Object[][] {},kolom));
        for(Transaksi kat:list){
            rowData[0]=kat.getIdTransaksi();
            rowData[1]=kat.getCustomer().getNamaCustomer();
            rowData[2]=kat.getMenu().getNamaMenu();
            rowData[3]=kat.getQuantity();
            rowData[4]=kat.getTanggal();
            rowData[5]=kat.getMenu().getHarga()*kat.getQuantity();
            ((DefaultTableModel)tableTransaksi.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword){
        String[] kolom={"id","Nama","Menu","quantity","tanggal","total"};
        ArrayList<Transaksi> list = new Transaksi().search(keyword);
        Object rowData[]=new Object[6];
        
        tableTransaksi.setModel(new DefaultTableModel(new Object[][] {},kolom));
        
        for(Transaksi kat:list){
            rowData[0]=kat.getIdTransaksi();
            rowData[1]=kat.getCustomer().getNamaCustomer();
            rowData[2]=kat.getMenu().getNamaMenu();
            rowData[3]=kat.getQuantity();
            rowData[4]=kat.getTanggal();
            rowData[5]=kat.getMenu().getHarga()*kat.getQuantity();
            
            ((DefaultTableModel)tableTransaksi.getModel()).addRow(rowData);
        }
        
    }
